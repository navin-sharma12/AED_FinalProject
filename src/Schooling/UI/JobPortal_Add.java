    private void jButtonSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSubmitActionPerformed
        // TODO add your handling code here:
        String company_name, job_title, job_type, location, job_description, category;
        
        company_name = jTextFieldCompanyName.getText();
        if(company_name.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Company name cannot be null.");
        }
        else
        {
            job_title = jTextFieldJobTitle.getText();
            if(job_title.isEmpty())
            {
                JOptionPane.showMessageDialog(this, "Job title cannot be null.");
            }
            else
            {
                category = jComboBoxCategory.getSelectedItem().toString();
                if("Off Campus".equals(category))
                {
                    job_type = jComboBoxJobType.getSelectedItem().toString();
                }
                else
                {
                    job_type = "";
                }
                location = jTextFieldLocation.getText();
                if (location.isEmpty())
                {
                    JOptionPane.showMessageDialog(this, "Location cannot be null.");
                } 
                else 
                {
                    job_description = jTextAreaJobDescription.getText();
                    if (job_description.isEmpty()) 
                    {
                        JOptionPane.showMessageDialog(this, "Job description cannot be null.");
                    } 
                    else 
                    {
                        try 
                        {
                            JobPortal jp = new JobPortal(company_name, job_title, category, job_type, location, job_description);
                            jp.addJob(company_name, job_title, category, job_type, location, job_description);
                        } 
                        catch (SQLException ex)
                        {
//                            Logger.getLogger(JobPortal_Add.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_jButtonSubmitActionPerformed

    private void jComboBoxCategoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxCategoryActionPerformed
        // TODO add your handling code here:
        String category;
        
        category = jComboBoxCategory.getSelectedItem().toString();
        if("Off Campus".equals(category))
        {
            jComboBoxJobType.addItem("Part time");
            jComboBoxJobType.addItem("Full time");
            jComboBoxJobType.addItem("Intern");
        }
        else
        {
            jComboBoxJobType.removeAllItems();
        }
    }//GEN-LAST:event_jComboBoxCategoryActionPerformed


